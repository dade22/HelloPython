# Math is what you would expect
1 + 1 #=> 2
8 - 1 #=> 7
10 * 2 #=> 20
35 / 5 #=> 7
2 ** 5 #=> 32
5 % 3 #=> 2 #mod

# Python automatically floors the results of Integer division (like Java)
5 / 2 #=> 2

# Use floats to fix this:
2.0     # This is a float
5.0 / 2.0 #=> 2.5

# Enforce precedence with parentheses
(1 + 3) * 2 #=> 8